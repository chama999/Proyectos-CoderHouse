//crear una calculadora
function sumar(a,b){
    return a+b;
}
function restar(a,b){
    return a-b;
}
function multiplicar(a,b){
    return a*b;
}
function dividir(a,b){
    return a/b;
}
function potencia(a,b){
    return Math.pow(a,b);
}
function raiz(a){
    return Math.sqrt(a);
}
function redondear(a){
    return Math.round(a);
}
function redondear_a_entero_mas_cercano(a){
    return Math.ceil(a);
}
function redondear_a_entero_mas_lejano(a){
    return Math.floor(a);
}
function seno(a){
    return Math.sin(a);
}
function coseno(a){
    return Math.cos(a);
}
function tangente(a){
    return Math.tan(a);
}
function logaritmo(a){
    return Math.log(a);
}
function logaritmo_base_10(a){
    return Math.log10(a);
}
function exponencial(a){
    return Math.exp(a);
}
function absoluto(a){
    return Math.abs(a);
}
function seno_inverso(a){
    return Math.asin(a);
}
function coseno_inverso(a){
    return Math.acos(a);
}
